func TestUniqueSorted(t *testing.T) {
	input := []string{"b", "a", "c", "a", "b", "c"}
	expected := []string{"a", "b", "c"}

	result := uniqueSorted(input)
	if len(result) != len(expected) {
		t.Fatalf("expected len %d, got %d", len(expected), len(result))
	}
	for i := range expected {
		if result[i] != expected[i] {
			t.Errorf("at index %d: expected %s, got %s", i, expected[i], result[i])
		}
	}
}

func TestUniqueSortedEmpty(t *testing.T) {
	result := uniqueSorted([]string{})
	if len(result) != 0 {
		t.Errorf("expected empty slice, got %v", result)
	}
}

