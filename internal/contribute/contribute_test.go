func TestRunWithTodoFiles(t *testing.T) {
	tmpDir := t.TempDir()
	ctx := context.Background()

	srcDir := filepath.Join(tmpDir, "internal", "test")
	if err := os.MkdirAll(srcDir, 0755); err != nil {
		t.Fatalf("cannot create dir: %v", err)
	}

	todoContent := []byte("package test\n\n// TODO: implement this\nfunc Foo() {}\n")
	todoPath := filepath.Join(srcDir, "todo.go")
	if err := os.WriteFile(todoPath, todoContent, 0644); err != nil {
		t.Fatalf("cannot write todo.go: %v", err)
	}

	report, err := Run(ctx, tmpDir)
	if err != nil {
		t.Fatalf("Run failed: %v", err)
	}

	found := false
	for _, s := range report.GoodFirstIssues.Suggested {
		if strings.Contains(s, "TODO") || strings.Contains(s, "FIXME") {
			found = true
		}
	}
	if !found {
		t.Errorf("expected TODO/FIXME suggestion, got %v", report.GoodFirstIssues.Suggested)
	}
}

