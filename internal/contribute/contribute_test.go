func TestRunCreatesContributingUserMD(t *testing.T) {
	tmpDir := t.TempDir()
	ctx := context.Background()

	report, err := Run(ctx, tmpDir)
	if err != nil {
		t.Fatalf("Run failed: %v", err)
	}

	if report.Status != "ok" {
		t.Errorf("expected status ok, got %s", report.Status)
	}

	outputPath := filepath.Join(tmpDir, "CONTRIBUTING_USER.md")
	if _, err := os.Stat(outputPath); os.IsNotExist(err) {
		t.Fatalf("CONTRIBUTING_USER.md not created")
	}

	data, err := os.ReadFile(outputPath)
	if err != nil {
		t.Fatalf("cannot read output: %v", err)
	}
	content := string(data)

	expectedSections := []string{
		"# Contributing (Generated by ForgeZero)",
		"## Environment check",
		"## Running tests",
		"## Local build",
		"## Good first issues",
		"## Mentoring mode",
		"## PR guidelines",
	}
	for _, section := range expectedSections {
		if !strings.Contains(content, section) {
			t.Errorf("missing section: %s", section)
		}
	}
}

func TestRunWithGoModSuggestsGoCommands(t *testing.T) {
	tmpDir := t.TempDir()
	ctx := context.Background()

	goModPath := filepath.Join(tmpDir, "go.mod")
	if err := os.WriteFile(goModPath, []byte("module test\n"), 0644); err != nil {
		t.Fatalf("cannot create go.mod: %v", err)
	}

	report, err := Run(ctx, tmpDir)
	if err != nil {
		t.Fatalf("Run failed: %v", err)
	}

	foundGoTest := false
	foundGoBuild := false
	for _, s := range report.Tests.Suggested {
		if strings.Contains(s, "go test") {
			foundGoTest = true
		}
	}
	for _, s := range report.Build.Suggested {
		if strings.Contains(s, "go build") {
			foundGoBuild = true
		}
	}

	if !foundGoTest {
		t.Errorf("expected go test suggestions, got %v", report.Tests.Suggested)
	}
	if !foundGoBuild {
		t.Errorf("expected go build suggestions, got %v", report.Build.Suggested)
	}
}

